package chapter13;

public interface Sub1a {

	boolean precedes(Object other);

	boolean follows(Object other);

	/*
	 * This is an interface. It contains method headings. This means that this
	 * interface doesn't define any methods. It only lists them. Also note that
	 * interfaces don't have instance variables, however they may have instance
	 * constants. An interface and all its method headings are normally declared
	 * public. In fact they cannot be given any other access. You may omit word
	 * public, they will be set public anyway.
	 */
}
